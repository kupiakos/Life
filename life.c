void reset_simulation()
{
	WDT_Sec_Cnt = WDT_1SEC_CNT;
	thisgen = state;
	nextgen = state + GENSEP;
	*thisgen = 0;
	memset(state, 0, sizeof(state));
	show_splash();
}

