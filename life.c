void cell_spawn(register uint8 row, register uint8 col)
{
	register sbyte *p;
	// Find the correct group
	for (p = thisgen; *p < 0 || *p > row; ++p);
	if (*p == row)
	{
		register sbyte repl1, repl2;

		while (-(*++p) > col);
		if (*p == -col)
			return; // Nothing to do if it already exists.
		repl1 = -col;

		do
		{
			repl2 = *p;
			*p = repl1;
			repl1 = repl2;
		} while (*p++);
	}
	else
	{
		register sbyte new1, new2, repl1, repl2, *n;
		int cont = 0;
		new1 = row;
		new2 = -col;
		n = p;
		do
		{
			repl1 = *n++;
			repl2 = *n++;
			*p++ = new1;
			*p++ = new2;
			cont = new1 && new2;
			new1 = repl1;
			new2 = repl2;
		} while (cont);
	}
	draw_alive(row, col);
}

