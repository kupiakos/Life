// Some notes about the algorithm used:
// The list used stores y values as positive numbers and x values as negative
// numbers. A single y (positive) number represents the start of a row, and the
// y coordinate of that row. It is then followed by x (negative) coordinates
// representing live cells on that row.
// The y values and x values are sorted so that the absolute value is always
// decreasing.
// The list is terminated with a 0. As such, the grid starts at
// coordinate (1,1) and not at (0,0).
void step_simulation()
{
	register sbyte *cur = thisgen, *new = nextgen;
	register sbyte *prev, *next;
	// a bitmap of everything around a cell, including itself.
	register uint16 neighbors = 0;
	// next x, current y
	register sbyte nx, y;

	prev = next = cur;

	*new = 0;

	// Note this algorithm will go from y = max y to 0, and x = max x to 0.
	do
	{
		// was an x coordinate written?
		if (*new < 0)
			ip(new);

		if (prev == next)
		{
			// start a new row group
			if (*next == 0)
				break;
			y = (*next) + 1;
			ip(next);
		}
		else
		{
			// move to next row and see which to scan
			if (*prev == y--)
				ip(prev);
			if (*cur == y)
				ip(cur);
			if (*next == y - 1)
				if (y - 1)
					ip(next);
		}

		// write the new y coordinate
		*new = y;
		do
		{
			nx = *prev;
			if (nx > *cur)
				nx = *cur;
			if (nx > *next)
				nx = *next;

			// any more x values to parse?
			if (nx >= 0)
				break;

			neighbors = 0;
			do
			{
				if (nx)
				{
					// add the next column to the neighbors bitmap
					if (*next == nx)
					{
						neighbors |= 0x100;
						ip(next);
					}
					if (*cur == nx)
					{
						neighbors |= 0x80;
						ip(cur);
					}
					if (*prev == nx)
					{
						neighbors |= 0x40;
						ip(prev);
					}
				}



				if (nx >= -(COLS - 1) && y <= ROWS && y > 0)
				{
					if (bitmap_isalive(neighbors))
					{
						if (bitmap_fate(neighbors))
						{
							ip(new);
							*new = nx - 1;
						}
						else
							draw_dead(y, -(nx - 1)); // using next x
					}
					else if (bitmap_fate(neighbors))
					{
						draw_alive(y, -(nx - 1));
						ip(new);
						*new = nx - 1;
					}
				}
				if (!(neighbors = shiftleft3[neighbors]))
					break;
			} while (nx++);
		} while (1);
	} while (1);

	*new = 0; // terminate our list

	next = nextgen;
	// nextgen = state + ((nextgen - state) + GENSEP) % sizeof(state);
	nextgen = (sbyte*)((nextgen - state) + GENSEP);
	if (!((uint16)nextgen < sizeof(state)))
		nextgen -= sizeof(state);
	nextgen += (uint16)state;
	thisgen = next;
}

void main(void)
{
	init_system();

	while (1)
	{
		reset_simulation();
		choose_simulation();
		init_simulation();
		do
		{
			step_simulation();
			P4OUT ^= 0x40;
		} while (!display_results(++generation));
	}
}

